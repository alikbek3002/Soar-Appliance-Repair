import { NextResponse } from "next/server";

export const runtime = "nodejs";

// ── Mapbox Search Box proxy ───────────────────────────────────────────────
// The browser never sees the Mapbox token: the address-autocomplete component
// calls THIS route, and we forward to Mapbox server-side. That also lets us pin
// the service area (country + bounding box) here, so the client can't widen it.
//
// Two actions, both GET:
//   /api/address?action=suggest&q=<text>&session=<uuid>  → list of candidates
//   /api/address?action=retrieve&id=<mapbox_id>&session=<uuid> → full address
// The `session` (a UUID generated by the browser) groups a suggest+retrieve pair
// into one billed Mapbox session — keeps usage in the free tier.

const SEARCH_BASE = "https://api.mapbox.com/search/searchbox/v1";

// Service area: Streamwood, IL + the greater Chicago area (see lib/site.ts).
// proximity biases ranking toward the shop; bbox is a hard filter so only
// addresses in the metro show up (no noise from the rest of the US).
const PROXIMITY = "-88.1784,42.0256"; // lng,lat — BUSINESS.geo
const BBOX = "-88.7,41.40,-87.40,42.40"; // minLng,minLat,maxLng,maxLat — Chicago metro
const COUNTRY = "us";

// Light per-IP cap so a bot can't run up the Mapbox bill by hammering suggest.
const WINDOW_MS = 60_000;
const MAX_PER_WINDOW = 80;
const hits = new Map<string, number[]>();

function rateLimited(ip: string, now: number): boolean {
  const recent = (hits.get(ip) || []).filter((t) => now - t < WINDOW_MS);
  recent.push(now);
  hits.set(ip, recent);
  if (hits.size > 5000) {
    for (const [k, v] of hits) {
      if (v.every((t) => now - t >= WINDOW_MS)) hits.delete(k);
    }
  }
  return recent.length > MAX_PER_WINDOW;
}

function clientIp(req: Request): string {
  const xff = req.headers.get("x-forwarded-for");
  if (xff) return xff.split(",")[0].trim();
  return req.headers.get("x-real-ip") || "unknown";
}

export async function GET(req: Request) {
  const token = process.env.MAPBOX_TOKEN;
  if (!token) {
    // Don't 500 — let the form fall back to a plain text input gracefully.
    return NextResponse.json({ suggestions: [], features: [] }, { status: 200 });
  }

  if (rateLimited(clientIp(req), Date.now())) {
    return NextResponse.json({ error: "Too many requests." }, { status: 429 });
  }

  const url = new URL(req.url);
  const action = url.searchParams.get("action");
  const session = (url.searchParams.get("session") || "").slice(0, 60);
  if (!session) {
    return NextResponse.json({ error: "Missing session." }, { status: 400 });
  }

  try {
    if (action === "suggest") {
      const q = (url.searchParams.get("q") || "").trim().slice(0, 100);
      if (q.length < 3) return NextResponse.json({ suggestions: [] });

      const params = new URLSearchParams({
        q,
        access_token: token,
        session_token: session,
        language: "en",
        country: COUNTRY,
        proximity: PROXIMITY,
        bbox: BBOX,
        types: "address,street",
        limit: "6",
      });
      const r = await fetch(`${SEARCH_BASE}/suggest?${params}`, {
        signal: AbortSignal.timeout(8_000),
      });
      if (!r.ok) return NextResponse.json({ suggestions: [] }, { status: 200 });
      const data = await r.json();
      // Trim to just what the dropdown needs.
      const suggestions = (data.suggestions || []).map(
        (s: { mapbox_id: string; name: string; place_formatted?: string; full_address?: string }) => ({
          id: s.mapbox_id,
          name: s.name,
          place_formatted: s.place_formatted || "",
          full_address: s.full_address || "",
        })
      );
      return NextResponse.json({ suggestions });
    }

    if (action === "retrieve") {
      const id = (url.searchParams.get("id") || "").slice(0, 120);
      if (!id) return NextResponse.json({ feature: null }, { status: 400 });

      const params = new URLSearchParams({
        access_token: token,
        session_token: session,
      });
      const r = await fetch(`${SEARCH_BASE}/retrieve/${encodeURIComponent(id)}?${params}`, {
        signal: AbortSignal.timeout(8_000),
      });
      if (!r.ok) return NextResponse.json({ feature: null }, { status: 200 });
      const data = await r.json();
      const f = data.features?.[0];
      if (!f) return NextResponse.json({ feature: null });
      const p = f.properties || {};
      const ctx = p.context || {};
      return NextResponse.json({
        feature: {
          full_address: p.full_address || p.name || "",
          city: ctx.place?.name || "",
          state: ctx.region?.region_code || ctx.region?.name || "",
          postcode: ctx.postcode?.name || "",
          coordinates: p.coordinates || null, // { longitude, latitude }
        },
      });
    }

    return NextResponse.json({ error: "Unknown action." }, { status: 400 });
  } catch {
    // Network/timeout — degrade silently; the user can still type freely.
    return NextResponse.json({ suggestions: [], feature: null }, { status: 200 });
  }
}
